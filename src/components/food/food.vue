<template>
  <transition name="move">
    <div v-show="showFlag"  class="food" ref='food'>
      <div class="food-content">
         <div class="image-header">
            <img :src="food.image">
            <div class="back">
              <i class="icon-arrow_lift" @click="hide"></i>
            </div>
         </div>
         <div class="content">
           <h1 class="food-title">{{food.name}}</h1>
           <div class="food-detail">
             <span class="sell-count">月售{{food.sellCount}}份</span>
             <span class="food-rating">好评率{{food.rating}}%</span>
           </div>
           <div class="price">
             <span class="now-price">￥{{food.price}}</span><span class="old-price" v-show="food.oldPrice">￥{{food.oldPrice}}</span>
           </div>
           <div class="cartcontrol-wrapper">
             <cartcontrol :food="food"></cartcontrol>
           </div>
           <div class="buy" v-show="!food.count || food.count==0" @click.stop.prevent="addFirst">
             加入购物车
           </div>
         </div>
         <split v-show="food.info"></split>
         <div class="info" v-show="food.info">
           <h1 class="info-title">商品介绍</h1>
           <p class="info-text">{{food.info}}</p>
         </div>
         <split></split>
         <div class="rating">
           <h1 class="rating-title">商品评价</h1>
           <ratingselect :select-type="selectType" :only-content="onlyContent" :desc="desc" :ratings="food.ratings"></ratingselect>
         </div>
      </div>
      
    </div>
  </transition>
</template>
<script>
  import BScroll from 'better-scroll';
  import Vue from 'vue';
  import cartcontrol from 'components/cartcontrol/cartcontrol';
  import split from 'components/split/split';
  import ratingselect from 'components/ratingselect/ratingselect';
  // const POSITIVE = 0;
// const NEGATIVE = 1;
const ALL = 2;
  export default{
    props: {
      food: {
        type: Object
      }
    },
    data () {
      return {
        showFlag: false,
        selectType: ALL,
        onlyContent: true,
        desc: {
          all: '全部',
          positive: '推荐',
          negative: '吐槽'
        }
      };
    },
    methods: {
      show () {
        this.showFlag = true;
        this.selectType = ALL;
        this.onlyContent = true;
        this.$nextTick(() => {
          if (!this.scroll) {
            this.scroll = new BScroll(this.$refs.food, {
              click: true
            });
          } else {
            this.scroll.refresh();
          }
        });
      },
      hide () {
        this.showFlag = false;
      },
      addFirst (event) {
        if (!event._constructed) {
          return;
        }
        Vue.set(this.food, 'count', 1);
      }
    },
    components: {
      cartcontrol,
      split,
      ratingselect
    }
  };
</script>
